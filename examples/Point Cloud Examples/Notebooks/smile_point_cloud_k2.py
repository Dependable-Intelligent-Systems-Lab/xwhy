class PointCloudData(Dataset):
    """Custom Dataset for loading point cloud data from OFF files."""

    def __init__(
        self,
        root_dir: Path,
        valid: bool = False,
        folder: str = "train",
        transform=None,
    ) -> None:
        """
        Initialize dataset.

        Args:
            root_dir (Path): Root directory of dataset.
            valid (bool, optional): Whether to use validation transforms. Defaults to False.
            folder (str, optional): Subfolder name (e.g., 'train', 'test'). Defaults to 'train'.
            transform (optional): Transform pipeline. Defaults to default_transforms().
        """
        self.root_dir = root_dir
        self.valid = valid

        folders = [
            directory
            for directory in sorted(os.listdir(root_dir))
            if os.path.isdir(root_dir / directory)
        ]

        self.classes: Dict[str, int] = {
            class_name: idx for idx, class_name in enumerate(folders)
        }

        self.transforms = transform if not valid else default_transforms()

        self.files: List[Dict[str, Any]] = []

        for category in self.classes.keys():
            category_dir = root_dir / Path(category) / folder

            for file_name in os.listdir(category_dir):
                if file_name.endswith(".off"):
                    self.files.append(
                        {
                            "pcd_path": category_dir / file_name,
                            "category": category,
                        }
                    )

    def __len__(self) -> int:
        """Return number of samples."""
        return len(self.files)

    def _preprocess(self, file_path) -> Any:
        """
        Load and preprocess a single OFF file.

        Args:
            file_path: File path.

        Returns:
            Any: Processed point cloud.
        """
        data = read_off(file_path)
        vertices = data.pos.numpy()
        faces = data.face.numpy().T

        if self.transforms:
            return self.transforms((vertices, faces))

        return vertices, faces

    def __getitem__(self, index: int) -> Dict[str, Any]:
        """
        Get a dataset sample.

        Args:
            index (int): Sample index.

        Returns:
            Dict[str, Any]: Dictionary with pointcloud and category label.
        """
        sample_info = self.files[index]
        pointcloud_path = sample_info["pcd_path"]
        category_name = sample_info["category"]

        point_cloud = self._preprocess(pointcloud_path)

        return {
            "pointcloud": point_cloud,
            "category": self.classes[category_name],
        }

