from scipy.stats import anderson


def calculate_anderson_darling_distance(
    original_cloud,
    perturbed_cloud,
) -> float:
    """
    Compute Anderson-Darling statistic between two point clouds.

    Args:
        original_cloud: Original point cloud.
        perturbed_cloud: Perturbed point cloud.

    Returns:
        float: Anderson-Darling statistic.
    """
    if isinstance(original_cloud, torch.Tensor):
        original_cloud = original_cloud.detach().cpu().numpy()
    if isinstance(perturbed_cloud, torch.Tensor):
        perturbed_cloud = perturbed_cloud.detach().cpu().numpy()

    original_flat = original_cloud.flatten()
    perturbed_flat = perturbed_cloud.flatten()

    statistic, _, _ = anderson(
        np.concatenate([original_flat, perturbed_flat])
    )

    return statistic

