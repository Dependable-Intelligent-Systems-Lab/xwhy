import torch
import torch.nn as nn
import torch.nn.functional as F


class TNet(nn.Module):
    """Transformation network used in PointNet."""

    def __init__(self, k: int = 3) -> None:
        """
        Initialize T-Net.

        Args:
            k (int): Input feature dimension.
        """
        super().__init__()
        self.k = k

        self.conv1 = nn.Conv1d(k, 64, 1)
        self.conv2 = nn.Conv1d(64, 128, 1)
        self.conv3 = nn.Conv1d(128, 1024, 1)

        self.fc1 = nn.Linear(1024, 512)
        self.fc2 = nn.Linear(512, 256)
        self.fc3 = nn.Linear(256, k * k)

        self.bn1 = nn.BatchNorm1d(64)
        self.bn2 = nn.BatchNorm1d(128)
        self.bn3 = nn.BatchNorm1d(1024)
        self.bn4 = nn.BatchNorm1d(512)
        self.bn5 = nn.BatchNorm1d(256)

    def forward(self, inputs: torch.Tensor) -> torch.Tensor:
        """
        Forward pass.

        Args:
            inputs (torch.Tensor): Input tensor of shape (B, k, N).

        Returns:
            torch.Tensor: Transformation matrix of shape (B, k, k).
        """
        batch_size = inputs.size(0)

        x = F.relu(self.bn1(self.conv1(inputs)))
        x = F.relu(self.bn2(self.conv2(x)))
        x = F.relu(self.bn3(self.conv3(x)))

        x = nn.MaxPool1d(x.size(-1))(x)
        x = nn.Flatten(1)(x)

        x = F.relu(self.bn4(self.fc1(x)))
        x = F.relu(self.bn5(self.fc2(x)))

        identity = torch.eye(self.k, requires_grad=True).repeat(batch_size, 1, 1)
        if inputs.is_cuda:
            identity = identity.cuda()

        matrix = self.fc3(x).view(-1, self.k, self.k) + identity
        return matrix

"""## Model: Transform Block"""

class Transform(nn.Module):
    """Feature transformation block used in PointNet."""

    def __init__(self) -> None:
        """Initialize transform module."""
        super().__init__()

        self.input_transform = TNet(k=3)
        self.feature_transform = TNet(k=64)

        self.conv1 = nn.Conv1d(3, 64, 1)
        self.conv2 = nn.Conv1d(64, 128, 1)
        self.conv3 = nn.Conv1d(128, 1024, 1)

        self.bn1 = nn.BatchNorm1d(64)
        self.bn2 = nn.BatchNorm1d(128)
        self.bn3 = nn.BatchNorm1d(1024)

    def forward(self, inputs: torch.Tensor):
        """
        Forward pass.

        Args:
            inputs (torch.Tensor): Input tensor of shape (B, 3, N).

        Returns:
            Tuple[torch.Tensor, torch.Tensor, torch.Tensor]:
                Features, input transform, feature transform.
        """
        matrix3x3 = self.input_transform(inputs)

        x = torch.bmm(inputs.transpose(1, 2), matrix3x3).transpose(1, 2)
        x = F.relu(self.bn1(self.conv1(x)))

        matrix64x64 = self.feature_transform(x)

        x = torch.bmm(x.transpose(1, 2), matrix64x64).transpose(1, 2)
        x = F.relu(self.bn2(self.conv2(x)))
        x = self.bn3(self.conv3(x))

        x = nn.MaxPool1d(x.size(-1))(x)
        x = nn.Flatten(1)(x)

        return x, matrix3x3, matrix64x64

"""## Model: PointNet"""

class PointNet(nn.Module):
    """PointNet classification model."""

    def __init__(self, num_classes: int = 40) -> None:
        """
        Initialize PointNet.

        Args:
            num_classes (int): Number of output classes.
        """
        super().__init__()

        self.transform = Transform()

        self.fc1 = nn.Linear(1024, 512)
        self.fc2 = nn.Linear(512, 256)
        self.fc3 = nn.Linear(256, num_classes)

        self.bn1 = nn.BatchNorm1d(512)
        self.bn2 = nn.BatchNorm1d(256)

        self.dropout = nn.Dropout(p=0.3)
        self.log_softmax = nn.LogSoftmax(dim=1)

    def forward(self, inputs: torch.Tensor):
        """
        Forward pass.

        Args:
            inputs (torch.Tensor): Input tensor of shape (B, 3, N).

        Returns:
            Tuple[torch.Tensor, torch.Tensor, torch.Tensor]:
                Log probabilities and transformation matrices.
        """
        x, matrix3x3, matrix64x64 = self.transform(inputs)

        x = F.relu(self.bn1(self.fc1(x)))
        x = F.relu(self.bn2(self.dropout(self.fc2(x))))

        x = self.fc3(x)

        return self.log_softmax(x), matrix3x3, matrix64x64

"""## Loss Function"""

def pointnet_loss(
    outputs: torch.Tensor,
    labels: torch.Tensor,
    matrix3x3: torch.Tensor,
    matrix64x64: torch.Tensor,
    alpha: float = 1e-4,
) -> torch.Tensor:
    """
    Compute PointNet loss with regularization.

    Args:
        outputs (torch.Tensor): Model predictions.
        labels (torch.Tensor): Ground truth labels.
        matrix3x3 (torch.Tensor): Input transform matrix.
        matrix64x64 (torch.Tensor): Feature transform matrix.
        alpha (float): Regularization weight.

    Returns:
        torch.Tensor: Loss value.
    """
    criterion = nn.NLLLoss()
    batch_size = outputs.size(0)

    identity3x3 = torch.eye(3, requires_grad=True).repeat(batch_size, 1, 1)
    identity64x64 = torch.eye(64, requires_grad=True).repeat(batch_size, 1, 1)

    if outputs.is_cuda:
        identity3x3 = identity3x3.cuda()
        identity64x64 = identity64x64.cuda()

    diff3x3 = identity3x3 - torch.bmm(matrix3x3, matrix3x3.transpose(1, 2))
    diff64x64 = identity64x64 - torch.bmm(matrix64x64, matrix64x64.transpose(1, 2))

    return criterion(outputs, labels) + alpha * (
        torch.norm(diff3x3) + torch.norm(diff64x64)
    ) / float(batch_size)

