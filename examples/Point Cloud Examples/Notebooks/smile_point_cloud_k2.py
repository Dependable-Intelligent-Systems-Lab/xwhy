from sklearn.metrics import pairwise_distances


def calculate_mask_cosine_distances(
    sample_input: np.ndarray,
    point_masks: List[np.ndarray],
) -> np.ndarray:
    """
    Compute cosine distances between full-cluster mask and perturbed cluster masks.

    This function measures similarity in the interpretable (cluster mask) space,
    where each mask represents which clusters are active (1) or removed (0).

    Args:
        sample_input (np.ndarray):
            Original point cloud used only to infer number of clusters/features.
        point_masks (List[np.ndarray]):
            List of binary masks for each perturbation.

    Returns:
        np.ndarray:
            Cosine distance between each perturbation mask and full mask.
    """
    # Full mask: all clusters are active (baseline explanation state)
    original_mask = np.ones(sample_input.shape[0])

    # Convert list of masks into matrix form (num_samples x num_clusters)
    masks_np = np.array(point_masks)

    # Compute cosine distances in mask space
    distances = pairwise_distances(
        masks_np,
        original_mask.reshape(1, -1),
        metric="cosine",
    ).ravel()

    return distances

"""### Latent Cosine Distance"""

from sklearn.metrics import pairwise_distances


def calculate_latent_cosine_distance(
    original_latent: Union[np.ndarray, torch.Tensor],
    perturbed_latent: Union[np.ndarray, torch.Tensor],
) -> float:
    """
    Compute cosine distance between original and perturbed latent representations.

    This measures how much the model's internal feature representation changes
    between the original input and a perturbed version.

    Args:
        original_latent (np.ndarray | torch.Tensor):
            Latent vector of the original input with shape (D,) or (1, D).
        perturbed_latent (np.ndarray | torch.Tensor):
            Latent vector of the perturbed input with shape (D,) or (1, D).

    Returns:
        float:
            Cosine distance between the two latent vectors.
    """
    # Convert torch tensors to numpy if needed
    if isinstance(original_latent, torch.Tensor):
        original_latent = original_latent.detach().cpu().numpy()
    if isinstance(perturbed_latent, torch.Tensor):
        perturbed_latent = perturbed_latent.detach().cpu().numpy()

    # Ensure correct shape: (1, D)
    original_latent = original_latent.reshape(1, -1)
    perturbed_latent = perturbed_latent.reshape(1, -1)

    # Compute cosine distance
    distance = pairwise_distances(
        perturbed_latent,
        original_latent,
        metric="cosine",
    ).ravel()[0]

    return float(distance)

"""### Wasserstein Distance"""

from scipy.stats import wasserstein_distance


def calculate_wasserstein_distance(
    point_cloud1,
    point_cloud2,
    mode: Literal["spatial", "latent"] = "spatial",
) -> float:
    """
    Compute Wasserstein distance between two point clouds.

    Args:
        point_cloud1: First point cloud (Tensor or ndarray).
        point_cloud2: Second point cloud (Tensor or ndarray).
        mode (str):
            - "spatial": compute per-axis distance (x, y, z)
            - "latent": compute on flattened representation

    Returns:
        float: Wasserstein distance.
    """
    # Convert to numpy safely
    if isinstance(point_cloud1, torch.Tensor):
        point_cloud1 = point_cloud1.detach().cpu().numpy()
    if isinstance(point_cloud2, torch.Tensor):
        point_cloud2 = point_cloud2.detach().cpu().numpy()

    if mode == "spatial":
        x1, y1, z1 = point_cloud1[:, 0], point_cloud1[:, 1], point_cloud1[:, 2]
        x2, y2, z2 = point_cloud2[:, 0], point_cloud2[:, 1], point_cloud2[:, 2]

        wd_x = wasserstein_distance(x1, x2)
        wd_y = wasserstein_distance(y1, y2)
        wd_z = wasserstein_distance(z1, z2)

        return wd_x + wd_y + wd_z

    elif mode == "latent":
        return wasserstein_distance(
            point_cloud1.flatten(),
            point_cloud2.flatten(),
        )

    else:
        raise ValueError("mode must be either 'spatial' or 'latent'")

