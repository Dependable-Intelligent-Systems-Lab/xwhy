class PointSampler:
    """Sample points uniformly from a mesh surface using triangle areas."""

    def __init__(self, output_size: int) -> None:
        """
        Initialize the sampler.

        Args:
            output_size (int): Number of points to sample.
        """
        assert isinstance(output_size, int)
        self.output_size = output_size

    def triangle_area(
        self,
        point1: np.ndarray,
        point2: np.ndarray,
        point3: np.ndarray,
    ) -> float:
        """
        Compute the area of a triangle defined by three points.

        Args:
            point1 (np.ndarray): First vertex.
            point2 (np.ndarray): Second vertex.
            point3 (np.ndarray): Third vertex.

        Returns:
            float: Triangle area.
        """
        # Check if any input point coordinates are non-finite (NaN or Inf)
        if not (np.all(np.isfinite(point1)) and np.all(np.isfinite(point2)) and np.all(np.isfinite(point3))):
            return 0.0

        side_a = np.linalg.norm(point1 - point2)
        side_b = np.linalg.norm(point2 - point3)
        side_c = np.linalg.norm(point3 - point1)

        # Check if side lengths are non-finite or non-positive
        if not (np.isfinite(side_a) and np.isfinite(side_b) and np.isfinite(side_c)) or \
           side_a <= 0 or side_b <= 0 or side_c <= 0:
            return 0.0

        semi_perimeter = 0.5 * (side_a + side_b + side_c)

        # Heron's formula intermediate calculation
        arg_sqrt = semi_perimeter * (
            (semi_perimeter - side_a)
            * (semi_perimeter - side_b)
            * (semi_perimeter - side_c)
        )

        # If arg_sqrt is negative due to floating point precision for degenerate triangles,
        # or if it's NaN/Inf due to previous operations, treat area as 0.
        if np.isnan(arg_sqrt) or np.isinf(arg_sqrt) or arg_sqrt < 0:
            return 0.0

        return arg_sqrt ** 0.5

    def sample_point(
        self,
        point1: np.ndarray,
        point2: np.ndarray,
        point3: np.ndarray,
    ) -> Tuple[float, float, float]:
        """
        Sample a random point inside a triangle using barycentric coordinates.

        Args:
            point1 (np.ndarray): First vertex.
            point2 (np.ndarray): Second vertex.
            point3 (np.ndarray): Third vertex.

        Returns:
            Tuple[float, float, float]: Sampled 3D point.
        """
        s, t = sorted([random.random(), random.random()])

        def interpolate(index: int) -> float:
            return (
                s * point1[index]
                + (t - s) * point2[index]
                + (1 - t) * point3[index]
            )

        return (interpolate(0), interpolate(1), interpolate(2))

    def __call__(
        self,
        mesh: Tuple[np.ndarray, np.ndarray],
    ) -> np.ndarray:
        """
        Sample points from a mesh.

        Args:
            mesh (Tuple[np.ndarray, np.ndarray]): Tuple of vertices and faces.

        Returns:
            np.ndarray: Sampled point cloud of shape (output_size, 3).
        """
        vertices, faces = mesh
        vertices = np.array(vertices)

        areas = []
        for idx in range(len(faces)):
            face = faces[idx]
            try:
                # Ensure face indices are within bounds before accessing vertices
                if np.any(face < 0) or np.any(face >= len(vertices)):
                    area_val = 0.0
                else:
                    p1, p2, p3 = vertices[face[0]], vertices[face[1]], vertices[face[2]]
                    area_val = self.triangle_area(p1, p2, p3)
            except IndexError: # Catch potential errors if face indices are malformed in other ways
                area_val = 0.0
            areas.append(area_val)

        areas = np.array(areas)

        # Replace any remaining NaN/inf with 0 as a safeguard, though triangle_area should now handle it
        areas = np.nan_to_num(areas, nan=0.0, posinf=0.0, neginf=0.0)

        # If all areas are 0 (e.g., all triangles are degenerate), random.choices will fail.
        # Assign uniform weights in such a case to allow sampling (from a degenerate mesh).
        if np.sum(areas) == 0:
            if len(areas) > 0:
                areas = np.ones_like(areas)
            else: # No faces at all
                return np.zeros((self.output_size, 3)) # Return an empty point cloud

        # Sample indices of faces, not the faces directly
        sampled_faces_indices = random.choices(
            range(len(faces)),
            weights=areas,
            k=self.output_size,
        )

        sampled_points = np.zeros((self.output_size, 3))

        for idx, face_list_idx in enumerate(sampled_faces_indices):
            face = faces[face_list_idx]
            # Access vertices using the face indices
            p1, p2, p3 = vertices[face[0]], vertices[face[1]], vertices[face[2]]
            sampled_points[idx] = self.sample_point(p1, p2, p3)

        return sampled_points

"""## Transformers"""

class Normalize:
    """Normalize a point cloud to zero mean and unit radius."""

    def __call__(self, point_cloud: np.ndarray) -> np.ndarray:
        """
        Normalize the input point cloud.

        Args:
            point_cloud (np.ndarray): Input array of shape (N, 3).

        Returns:
            np.ndarray: Normalized point cloud.
        """
        assert len(point_cloud.shape) == 2

        normalized = point_cloud - np.mean(point_cloud, axis=0)
        normalized /= np.max(np.linalg.norm(normalized, axis=1))

        return normalized


class RandomRotationZ:
    """Apply a random rotation around the Z-axis."""

    def __call__(self, point_cloud: np.ndarray) -> np.ndarray:
        """
        Rotate the point cloud randomly around Z-axis.

        Args:
            point_cloud (np.ndarray): Input array of shape (N, 3).

        Returns:
            np.ndarray: Rotated point cloud.
        """
        assert len(point_cloud.shape) == 2

        theta = random.random() * 2.0 * math.pi

        rotation_matrix = np.array(
            [
                [math.cos(theta), -math.sin(theta), 0],
                [math.sin(theta), math.cos(theta), 0],
                [0, 0, 1],
            ]
        )

        return rotation_matrix.dot(point_cloud.T).T


class RandomNoise:
    """Add Gaussian noise to a point cloud."""

    def __call__(self, point_cloud: np.ndarray) -> np.ndarray:
        """
        Add random noise to the point cloud.

        Args:
            point_cloud (np.ndarray): Input array of shape (N, 3).

        Returns:
            np.ndarray: Noisy point cloud.
        """
        assert len(point_cloud.shape) == 2

        noise = np.random.normal(0, 0.02, point_cloud.shape)
        return point_cloud + noise

