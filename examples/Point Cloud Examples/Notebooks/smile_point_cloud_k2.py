def build_explainer_model_name(
    method: Literal["LIME", "SMILE"],
    surrogate_name: str,
    clustering_mode: str,
    distance_mode: str,
    distance_metric: str = "cosine",
) -> str:
    """
    Build a standardized model name for experiment comparison.

    Args:
        method (str): Explainer method ("LIME" or "SMILE").
        surrogate_name (str): Surrogate model name.
        clustering_mode (str): "kmeans" or "precomputed".
        distance_mode (str): "mask", "spatial", or "latent".
        distance_metric (str): Distance metric used.

    Returns:
        str: Formatted model name.
    """
    distance_abbr = {
        "cosine": "COS",
        "wasserstein": "WD",
        "ks": "KS",
        "anderson": "AD",
    }

    clustering_abbr = {
        "kmeans": "kmeans",
        "precomputed": "noise",
    }

    dist_tag = distance_abbr.get(distance_metric, distance_metric.upper())
    cluster_tag = clustering_abbr.get(clustering_mode, clustering_mode)

    return f"{method}-{dist_tag}-{cluster_tag}-{distance_mode} ({surrogate_name})"

"""### Unified LIME Method"""

def lime_explain(
    sample_input: torch.Tensor,
    sample_label: int,
    model: torch.nn.Module,
    num_clusters: int,
    num_top_features: int,
    num_perturbations: int,
    device: torch.device,
    kernel_width: float = 0.5,
    epsilon: float = 0.0,
    surrogate_model_type: Literal["linear", "bayesian"] = "linear",
    max_iters: int = 50,
    random_seed: int = 42,
    clustering_mode: Literal["kmeans", "precomputed"] = "kmeans",
    cluster_labels: Optional[np.ndarray] = None,
    distance_mode: Literal["mask", "latent"] = "mask",
) -> Tuple[np.ndarray, Dict[str, float], str]:
    """
    Unified LIME explanation method for point cloud models.

    Supports:
    - Standard LIME (k-means clustering + mask distance)
    - Noise-based LIME (precomputed clusters)
    - Latent-space LIME (distance in model feature space)

    Args:
        sample_input (torch.Tensor): Input point cloud (N, 3).
        sample_label (int): Ground truth label.
        model (torch.nn.Module): Trained model.
        num_clusters (int): Number of clusters.
        num_top_features (int): Number of important clusters to return.
        num_perturbations (int): Number of perturbations.
        device (torch.device): Computation device.
        kernel_width (float): Kernel width for weighting.
        epsilon (float): Small constant for numerical stability.
        surrogate_model_type (str): Type of surrogate model.
        max_iters (int): Max iterations for clustering.
        random_seed (int): Random seed.
        clustering_mode (str):
            "kmeans" => compute clusters
            "precomputed" => use provided cluster_labels
        cluster_labels (Optional[np.ndarray]):
            Required if clustering_mode="precomputed"
        distance_mode (str):
            "mask" => cosine distance in interpretable space
            "latent" => cosine distance in latent space

    Returns:
        Tuple[np.ndarray, Dict[str, float], str]:
            Top important clusters, fidelity metrics, and experiment model name.
    """
    # --------------------------------------------------
    # Step 0: Print model name
    # --------------------------------------------------

    surrogate_name = (
        "LinearRegression" if surrogate_model_type == "linear"
        else "BayesianRidge"
    )

    model_name = build_explainer_model_name(
        method="LIME",
        surrogate_name=surrogate_name,
        clustering_mode=clustering_mode,
        distance_mode=distance_mode,
        distance_metric="cosine",
    )

    print(model_name)

    # --------------------------------------------------
    # Step 1: Prediction
    # --------------------------------------------------
    pred, probabilities, top_pred_classes = predict_pointnet(
        model, sample_input, sample_label
    )

    # sample_input = sample_input.unsqueeze(0).float()

    # Ensure correct shape: (1, N, 3)
    if sample_input.ndim == 2:
        sample_input = sample_input.unsqueeze(0)

    sample_input = sample_input.float()

    sample_numpy_array = sample_input.cpu().numpy().squeeze(0)

    # --------------------------------------------------
    # Step 2: Clustering
    # --------------------------------------------------
    if clustering_mode == "kmeans":
        _, cluster_labels = kmeans_with_fps(
            sample_input,
            num_clusters,
            max_iters=max_iters,
            random_seed=random_seed,
        )
    elif clustering_mode == "precomputed":
        if cluster_labels is None:
            raise ValueError("cluster_labels must be provided for precomputed mode.")
    else:
        raise ValueError(f"Invalid clustering_mode: {clustering_mode}")

    # --------------------------------------------------
    # Step 3: Perturbation
    # --------------------------------------------------
    point_cloud = sample_input.squeeze(0)  # (N, 3)

    assert point_cloud.ndim == 2, f"Expected (N,3), got {point_cloud.shape}"

    perturbed_samples, masks, _, cluster_masks = (
        perturb_point_cloud_by_clusters(
            point_cloud,
            cluster_labels,
            num_perturbations,
            removal_probability,
            random_seed,
        )
    )

    # --------------------------------------------------
    # Step 4: Distance computation
    # --------------------------------------------------
    if distance_mode == "mask":
        distances = calculate_mask_cosine_distances(
            sample_input.squeeze(0).cpu().numpy(),
            masks,
        )

    elif distance_mode == "latent":
        _, original_latent, _ = predict_pointnet(
            model, sample_input.squeeze(0), sample_label
        )
        original_latent = original_latent.squeeze(0)

        distances = []
        for perturbed_sample in perturbed_samples:
            _, perturbed_latent, _ = predict_pointnet(
                model, perturbed_sample, sample_label
            )
            perturbed_latent = perturbed_latent.squeeze(0)

            dist = calculate_latent_cosine_distance(
                original_latent, perturbed_latent
            )
            distances.append(dist)

    else:
        raise ValueError(f"Invalid distance_mode: {distance_mode}")

    # --------------------------------------------------
    # Step 5: Weights
    # --------------------------------------------------
    weights = calculate_weights(distances, kernel_width, epsilon)

    # --------------------------------------------------
    # Step 6: Model probabilities
    # --------------------------------------------------
    output_probabilities = get_output_probabilities(
        model, perturbed_samples, device
    )

    # --------------------------------------------------
    # Step 7: Surrogate model
    # --------------------------------------------------
    coefficients, y_true, y_pred = calculate_surrogate_coefficients(
        model_type=surrogate_model_type,
        cluster_masks=cluster_masks,
        output_probabilities=output_probabilities,
        top_pred_classes=top_pred_classes,
        weights=weights,
    )

    metrics = calculate_fidelity_metrics(
        y_true=y_true,
        y_pred=y_pred,
        weights=weights,
        cluster_masks=cluster_masks,
        model_name=model_name,
        num_clusters=num_clusters,
        kernel_width=kernel_width,
        num_perturbations=num_perturbations,
    )

    print_fidelity_metrics(metrics)

    # --------------------------------------------------
    # Step 8: Feature selection
    # --------------------------------------------------
    top_features = np.argsort(coefficients)[-num_top_features:]

    # --------------------------------------------------
    # Step 9: Visualization
    # --------------------------------------------------
    fig = visualize_explanation_pointcloud(
        points=sample_numpy_array,
        cluster_labels=cluster_labels,
        important_clusters=top_features,
    )
    show_persistent_figure(fig)

    return top_features, metrics, model_name

"""### Unified SMILE Method"""

def smile_explain(
    sample_input: torch.Tensor,
    sample_label: int,
    model: torch.nn.Module,
    num_clusters: int,
    num_top_features: int,
    num_perturbations: int,
    device: torch.device,
    kernel_width: float = 0.5,
    epsilon: float = 0.0,
    surrogate_model_type: Literal["linear", "bayesian"] = "linear",
    max_iters: int = 50,
    random_seed: int = 42,
    clustering_mode: Literal["kmeans", "precomputed"] = "kmeans",
    cluster_labels: Optional[np.ndarray] = None,
    distance_metric: Literal["wasserstein", "ks", "anderson"] = "wasserstein",
    distance_mode: Literal["spatial", "latent"] = "spatial",
) -> Tuple[np.ndarray, Dict[str, float], str]:
    """
    Unified SMILE explanation method for point cloud models.

    Supports:
    - Spatial distances (point cloud space)
    - Latent distances (model feature space)
    - Multiple statistical distances (Wasserstein, KS, Anderson-Darling)

    Args:
        sample_input (torch.Tensor): Input point cloud (N, 3).
        sample_label (int): Ground truth label.
        model (torch.nn.Module): Trained model.
        num_clusters (int): Number of clusters.
        num_top_features (int): Number of important clusters.
        num_perturbations (int): Number of perturbations.
        device (torch.device): Computation device.
        kernel_width (float): Kernel width.
        epsilon (float): Numerical stability constant.
        surrogate_model_type (str): Surrogate model type.
        max_iters (int): Max clustering iterations.
        random_seed (int): Random seed.
        clustering_mode (str): "kmeans" or "precomputed".
        cluster_labels (Optional[np.ndarray]): Required for precomputed mode.
        distance_metric (str): "wasserstein", "ks", or "anderson".
        distance_mode (str): "spatial" or "latent".

    Returns:
        Tuple[np.ndarray, Dict[str, float], str]:
            Top important clusters, fidelity metrics, and experiment model name.
    """

    # --------------------------------------------------
    # Step 0: Print model name
    # --------------------------------------------------

    surrogate_name = (
        "LinearRegression" if surrogate_model_type == "linear"
        else "BayesianRidge"
    )

    model_name = build_explainer_model_name(
        method="SMILE",
        surrogate_name=surrogate_name,
        clustering_mode=clustering_mode,
        distance_mode=distance_mode,
        distance_metric=distance_metric,
    )

    print(model_name)

    # --------------------------------------------------
    # Step 1: Prediction
    # --------------------------------------------------
    pred, probabilities, top_pred_classes = predict_pointnet(
        model, sample_input, sample_label
    )

    # sample_input = sample_input.unsqueeze(0).float()
    # Ensure correct shape: (1, N, 3)
    if sample_input.ndim == 2:
        sample_input = sample_input.unsqueeze(0)

    sample_input = sample_input.float()

    sample_numpy_array = sample_input.cpu().numpy().squeeze(0)

    # --------------------------------------------------
    # Step 2: Clustering
    # --------------------------------------------------
    if clustering_mode == "kmeans":
        _, cluster_labels = kmeans_with_fps(
            sample_input,
            num_clusters,
            max_iters=max_iters,
            random_seed=random_seed,
        )
    elif clustering_mode == "precomputed":
        if cluster_labels is None:
            raise ValueError("cluster_labels must be provided.")
    else:
        raise ValueError(f"Invalid clustering_mode: {clustering_mode}")

    # --------------------------------------------------
    # Step 3: Perturbation
    # --------------------------------------------------
    point_cloud = sample_input.squeeze(0)  # (N, 3)

    assert point_cloud.ndim == 2, f"Expected (N,3), got {point_cloud.shape}"

    perturbed_samples, masks, _, cluster_masks = (
        perturb_point_cloud_by_clusters(
            point_cloud,
            cluster_labels,
            num_perturbations,
            removal_probability,
            random_seed,
        )
    )

    # --------------------------------------------------
    # Step 4: Distance computation (SMILE core)
    # --------------------------------------------------
    distances: List[float] = []

    if distance_mode == "spatial":
        original = sample_input.squeeze(0)

        for perturbed in perturbed_samples:
            if distance_metric == "wasserstein":
                dist = calculate_wasserstein_distance(original, perturbed)

            elif distance_metric == "ks":
                dist = calculate_ks_distance(original, perturbed)

            elif distance_metric == "anderson":
                dist = calculate_anderson_darling_distance(original, perturbed)

            else:
                raise ValueError(f"Invalid distance_metric: {distance_metric}")

            distances.append(dist)

    elif distance_mode == "latent":
        _, original_latent, _ = predict_pointnet(
            model, sample_input.squeeze(0), sample_label
        )
        original_latent = original_latent.squeeze(0)

        for perturbed in perturbed_samples:
            _, perturbed_latent, _ = predict_pointnet(
                model, perturbed, sample_label
            )
            perturbed_latent = perturbed_latent.squeeze(0)

            if distance_metric == "wasserstein":
                dist = calculate_wasserstein_distance(
                    original_latent, perturbed_latent, mode="latent"
                )

            elif distance_metric == "ks":
                dist = calculate_ks_distance(
                    original_latent, perturbed_latent
                )

            elif distance_metric == "anderson":
                dist = calculate_anderson_darling_distance(
                    original_latent, perturbed_latent
                )

            else:
                raise ValueError(f"Invalid distance_metric: {distance_metric}")

            distances.append(dist)

    else:
        raise ValueError(f"Invalid distance_mode: {distance_mode}")

    # --------------------------------------------------
    # Step 5: Weights
    # --------------------------------------------------
    weights = calculate_weights(distances, kernel_width, epsilon)

    # --------------------------------------------------
    # Step 6: Probabilities
    # --------------------------------------------------
    output_probabilities = get_output_probabilities(
        model, perturbed_samples, device
    )

    # --------------------------------------------------
    # Step 7: Surrogate model
    # --------------------------------------------------
    coefficients, y_true, y_pred = calculate_surrogate_coefficients(
        model_type=surrogate_model_type,
        cluster_masks=cluster_masks,
        output_probabilities=output_probabilities,
        top_pred_classes=top_pred_classes,
        weights=weights,
    )

    metrics = calculate_fidelity_metrics(
        y_true=y_true,
        y_pred=y_pred,
        weights=weights,
        cluster_masks=cluster_masks,
        model_name=model_name,
        num_clusters=num_clusters,
        kernel_width=kernel_width,
        num_perturbations=num_perturbations,
    )

    print_fidelity_metrics(metrics)

    # --------------------------------------------------
    # Step 8: Feature selection
    # --------------------------------------------------
    top_features = np.argsort(coefficients)[-num_top_features:]

    # --------------------------------------------------
    # Step 9: Visualization
    # --------------------------------------------------
    fig = visualize_explanation_pointcloud(
        points=sample_numpy_array,
        cluster_labels=cluster_labels,
        important_clusters=top_features,
    )
    show_persistent_figure(fig)

    return top_features, metrics, model_name

"""### SHAP Method"""

def shap_explain(
    model: torch.nn.Module,
    sample_input: np.ndarray,
    device: torch.device,
    marker_size: int = 4,
    title: str = "Point Cloud with SHAP Importance",
    use_summary_plot: bool = False,
) -> Tuple[np.ndarray, np.ndarray]:
    """Compute SHAP explanations for a PointNet model.

    Args:
        model (torch.nn.Module): Trained PointNet model.
        sample_input (np.ndarray): Input point cloud (N, 3).
        device (torch.device): Device for computation.
        marker_size (int): Marker size for visualization.
        title (str): Plot title.
        use_summary_plot (bool): Whether to show SHAP summary plot.

    Returns:
        Tuple[np.ndarray, np.ndarray]:
            - Normalized importance scores (N,)
            - SHAP values array (N, 3)
    """
    import shap

    class PointNetWrapper(torch.nn.Module):
        """Wrapper to make PointNet compatible with SHAP."""

        def __init__(self, base_model: torch.nn.Module):
            super().__init__()
            self.base_model = base_model

        def forward(self, x: torch.Tensor) -> torch.Tensor:
            output = self.base_model(x)
            return output[0] if isinstance(output, tuple) else output

    # --------------------------------------------------
    # Step 1: Prepare input
    # --------------------------------------------------
    model_wrapper = PointNetWrapper(model).to(device)

    input_tensor = torch.tensor(sample_input, dtype=torch.float32)
    input_tensor = input_tensor.unsqueeze(0).to(device)  # (1, N, 3)
    input_tensor = input_tensor.transpose(1, 2)          # (1, 3, N)

    background = input_tensor.clone().detach()

    # --------------------------------------------------
    # Step 2: SHAP Explainer
    # --------------------------------------------------
    explainer = shap.DeepExplainer(model_wrapper, background)
    shap_values = explainer.shap_values(input_tensor)

    # --------------------------------------------------
    # Step 3: Select predicted class
    # --------------------------------------------------
    with torch.no_grad():
        output = model_wrapper(input_tensor)
        pred_class = torch.argmax(output, dim=1).item()

    # SHAP returns list per class OR array with class dim
    if isinstance(shap_values, list):
        shap_array = shap_values[pred_class]  # (1, 3, N)
    else:
        shap_array = shap_values[..., pred_class]  # (1, 3, N)

    # --------------------------------------------------
    # Step 4: Reshape to (N, 3)
    # --------------------------------------------------
    if shap_array.ndim == 3:
        shap_array = shap_array.squeeze(0)        # (3, N)
        shap_array = shap_array.transpose(1, 0)   # (N, 3)
    else:
        raise ValueError(f"Unexpected SHAP shape: {shap_array.shape}")

    # --------------------------------------------------
    # Step 5: Features
    # --------------------------------------------------
    features = input_tensor.squeeze(0).cpu().numpy().transpose(1, 0)  # (N, 3)

    # --------------------------------------------------
    # Step 6: Optional summary plot
    # --------------------------------------------------
    if use_summary_plot:
        shap.summary_plot(
            shap_array,
            features=features,
            feature_names=["x", "y", "z"],
        )

    # --------------------------------------------------
    # Step 7: Importance normalization
    # --------------------------------------------------
    importance = np.sum(np.abs(shap_array), axis=1)

    min_val = importance.min()
    max_val = importance.max()

    if max_val - min_val > 0:
        importance_norm = (importance - min_val) / (max_val - min_val)
    else:
        importance_norm = np.full_like(importance, 0.5)

    # --------------------------------------------------
    # Step 8: Visualization
    # --------------------------------------------------
    fig = create_colored_point_cloud_figure(
        vertices=features,
        importance=importance_norm,
        marker_size=marker_size,
        title=title,
    )
    show_persistent_figure(fig)

    return importance_norm, shap_array

"""## Step 10: Comparing Explanations and Plotting Comparison Results

### Helper: Run Single Experiment
"""

def run_experiment(
    explain_fn: Callable[..., Tuple[np.ndarray, Dict[str, float], str]],
    explain_kwargs: Dict[str, Any],
    fidelity_scores: list,
    running_times: list,
    all_top_features: list,
) -> None:
    """Run a single explanation experiment and collect results.

    Args:
        explain_fn (Callable): Explanation function (LIME or SMILE).
        explain_kwargs (Dict[str, Any]): Arguments for the explain function.
        fidelity_scores (list): List to store fidelity metrics.
        running_times (list): List to store execution times.
        all_top_features (list): List to store top features.
    """
    start_time = time.time()

    results, metrics, model_name = explain_fn(**explain_kwargs)

    end_time = time.time()

    fidelity_scores.append(metrics)
    all_top_features.append(results)

    running_time_dict = {
        "name": model_name,
        "time": end_time - start_time,
    }

    print(running_time_dict)
    running_times.append(running_time_dict)

"""### Helper: Plot Fidelity Comparison"""

def plot_fidelity_comparison(
    fidelity_scores: List[dict],
    x_column: str,
    model_filters: Optional[List[str]] = None,
    figure_name: str = "comparison",
) -> None:
    """Plot fidelity metrics comparison for multiple explanation methods.

    Args:
        fidelity_scores (List[dict]): List of fidelity metric dictionaries.
        x_column (str): Column name for x-axis (e.g., "kernel_width").
        model_filters (Optional[List[str]]): List of model name substrings to filter.
        figure_name (str): Output SVG file name (without extension).
    """
    fidelity_df = pd.DataFrame(fidelity_scores)

    if model_filters:
        mask = fidelity_df["name"].apply(
            lambda x: any(f in x for f in model_filters)
        )
        fidelity_df = fidelity_df[mask]

    fig, axs = plt.subplots(3, 2, figsize=(10, 10))

    metrics_config = [
        ("mean_l1", "Mean L1", True),
        ("mean_l2", "Mean L2", True),
        ("weighted_l1", "Weighted L1", True),
        ("weighted_l2", "Weighted L2", True),
        ("mean_loss", "Mean Loss", True),
        ("weighted_adj_r2", "Adjusted R2 Score", False),
    ]

    axes = axs.flatten()

    for ax, (metric, ylabel, use_log) in zip(axes, metrics_config):
        sns.lineplot(
            data=fidelity_df,
            x=x_column,
            y=metric,
            hue="name",
            ax=ax,
        )

        ax.set_ylabel(ylabel)
        ax.set_xlabel("" if metric != "weighted_adj_r2" else x_column)

        if use_log:
            ax.set_yscale("log")

        ax.grid(True, alpha=0.2)
        ax.legend()

    plt.tight_layout()

    output_path = f"./{figure_name}.svg"
    plt.savefig(output_path, format="svg")

    plt.show()

"""### Helper: Plot Running Time Comparison"""

def plot_running_time_comparison(
    linear_times: List[Dict[str, float]],
    bayesian_times: List[Dict[str, float]],
    x_column: str = "name",
    figure_name: str = "running_time_comparison",
    y_limits: Optional[tuple] = None,
) -> None:
    """Plot aggregated running time comparison for Linear and Bayesian models.

    This function aggregates running times by model name and plots both
    LinearRegression and BayesianRidge results on the same axis for
    easier visual comparison.

    Args:
        linear_times (List[Dict[str, float]]): Running time records for linear model.
        bayesian_times (List[Dict[str, float]]): Running time records for Bayesian model.
        x_column (str): Column used for grouping on x-axis (default: "name").
        figure_name (str): Output SVG file name (without extension).
        y_limits (Optional[tuple]): Optional y-axis limits as (min, max).
    """

    # --- Convert to DataFrame ---
    linear_df = pd.DataFrame(linear_times)
    bayesian_df = pd.DataFrame(bayesian_times)

    # --- Aggregate ---
    linear_df = linear_df.groupby(x_column, as_index=False)["time"].mean()
    bayesian_df = bayesian_df.groupby(x_column, as_index=False)["time"].mean()

    # --- Plot ---
    fig, ax = plt.subplots(figsize=(10, 6))

    sns.lineplot(
        data=linear_df,
        x=x_column,
        y="time",
        ax=ax,
        marker="o",
        label="Linear",
    )

    sns.lineplot(
        data=bayesian_df,
        x=x_column,
        y="time",
        ax=ax,
        marker="o",
        linestyle="dotted",
        label="Bayesian",
    )

    # --- Labels ---
    ax.set_ylabel("Running Time (seconds)")
    ax.set_xlabel("Model")

    # --- Optional limits ---
    if y_limits is not None:
        ax.set_ylim(*y_limits)

    # --- Improve readability ---
    plt.xticks(rotation=45, ha="right")

    # --- Layout ---
    plt.grid(alpha=0.2)
    plt.tight_layout()

    # --- Save ---
    plt.savefig(f"./{figure_name}.svg", format="svg")

    plt.show()

"""### Helper: Plot Bar Comparison"""

def plot_bar_comparison(
    left_df: pd.DataFrame,
    right_df: pd.DataFrame,
    metric: str = "weighted_adj_r2",
    x_column: str = "name",
    left_title: Optional[str] = "LIME",
    right_title: Optional[str] = "SMILE",
    figure_name: str = "bar_comparison",
    rotate_xticks: bool = False,
) -> None:
    """Plot side-by-side bar comparison for two datasets.

    Args:
        left_df (pd.DataFrame): First dataset (e.g., LIME results).
        right_df (pd.DataFrame): Second dataset (e.g., SMILE results).
        metric (str): Metric to plot on y-axis.
        x_column (str): Column name for x-axis.
        left_title (Optional[str]): Title for left subplot.
        right_title (Optional[str]): Title for right subplot.
        figure_name (str): Output SVG file name (without extension).
        rotate_xticks (bool): Whether to rotate x-axis labels.
    """
    fig, axes = plt.subplots(1, 2, figsize=(10, 5), sharey=True)

    # --- Left Plot ---
    sns.barplot(
        data=left_df,
        x=x_column,
        y=metric,
        ax=axes[0],
    )
    axes[0].set_xlabel("Model")
    axes[0].set_ylabel(metric.replace("_", " ").title())
    axes[0].set_title(left_title if left_title else "")
    axes[0].grid(alpha=0.2)

    # --- Right Plot ---
    sns.barplot(
        data=right_df,
        x=x_column,
        y=metric,
        ax=axes[1],
    )
    axes[1].set_xlabel("Model")
    axes[1].set_ylabel("")
    axes[1].set_title(right_title if right_title else "")
    axes[1].grid(alpha=0.2)

    # --- Optional Tick Rotation ---
    if rotate_xticks:
        for ax in axes:
            ax.set_xticklabels(ax.get_xticklabels(), rotation=45, ha="right")

    # --- Layout ---
    plt.tight_layout()

    output_path = f"./{figure_name}.svg"
    plt.savefig(output_path, format="svg")

    plt.show()

