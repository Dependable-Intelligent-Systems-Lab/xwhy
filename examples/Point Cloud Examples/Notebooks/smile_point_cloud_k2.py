def perturb_point_cloud_by_clusters(
    point_cloud: torch.Tensor,
    cluster_labels: np.ndarray,
    num_perturbations: int,
    removal_probability: float = 0.5,
    random_seed: int | None = None,
) -> Tuple[
    List[torch.Tensor],
    List[np.ndarray],
    List[int],
    np.ndarray,
]:
    """
    Generate perturbed point clouds by randomly removing clusters.

    Args:
        point_cloud (torch.Tensor): Input point cloud of shape (N, 3).
        cluster_labels (np.ndarray): Cluster label per point.
        num_perturbations (int): Number of perturbed samples.
        removal_probability (float): Probability of removing a cluster.
        random_seed (Optional[int]): Random seed.

    Returns:
        Tuple:
            - List of perturbed point clouds
            - List of point-level masks
            - All-ones mask (original sample)
            - Cluster-level masks (num_perturbations, num_clusters)
    """
    if random_seed is not None:
        np.random.seed(random_seed)

    num_points = point_cloud.size(0)
    unique_clusters = np.unique(cluster_labels)
    num_clusters = len(unique_clusters)

    perturbed_clouds: List[torch.Tensor] = []
    point_masks: List[np.ndarray] = []
    cluster_masks = np.zeros((num_perturbations, num_clusters), dtype=int)

    print(f"Number of perturbations: {num_perturbations}")

    for perturb_idx in range(num_perturbations):
        # Sample cluster mask (1 = keep, 0 = remove)
        cluster_mask = np.random.binomial(
            1, 1 - removal_probability, size=num_clusters
        )

        # Map cluster mask to each point
        point_mask = np.array(
            [cluster_mask[cluster_id] for cluster_id in cluster_labels]
        )

        # Select points to keep
        indices_to_keep = np.where(point_mask == 1)[0]
        perturbed_cloud = point_cloud[indices_to_keep]

        perturbed_clouds.append(perturbed_cloud)
        point_masks.append(point_mask)
        cluster_masks[perturb_idx] = cluster_mask

    all_ones_mask = [1] * num_points

    return perturbed_clouds, point_masks, all_ones_mask, cluster_masks

