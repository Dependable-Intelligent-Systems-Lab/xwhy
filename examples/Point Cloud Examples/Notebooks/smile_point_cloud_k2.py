"""## Step 5: Weights

### Kernel Weight Function
"""

def calculate_weights(
    distances: np.ndarray,
    kernel_width: float = 0.5,
    epsilon: float = 0.0,
) -> np.ndarray:
    """
    Compute weights from distances using an exponential kernel.

    Args:
        distances (np.ndarray): Distance values.
        kernel_width (float): Kernel width controlling decay.
        epsilon (float): Small constant for numerical stability.

    Returns:
        np.ndarray: Computed weights.
    """
    distances = np.asarray(distances)

    weights = np.sqrt(
        np.exp(-(distances ** 2) / (kernel_width ** 2))
    ) + epsilon

    return weights

"""## Step 6: Probabilities

### Probability Extraction Function
"""

def get_output_probabilities(
    model: torch.nn.Module,
    perturbed_samples: List[torch.Tensor],
    device: torch.device,
) -> torch.Tensor:
    """
    Compute output probabilities for perturbed samples.

    Args:
        model (torch.nn.Module): Trained model.
        perturbed_samples (List[torch.Tensor]): List of perturbed point clouds.
        device (torch.device): Computation device.

    Returns:
        torch.Tensor: Tensor of shape (num_samples, 1, num_classes).
    """
    model.eval()

    probabilities_list: List[torch.Tensor] = []

    with torch.no_grad():
        for sample in perturbed_samples:
            input_batch = sample.unsqueeze(0).float().to(device)

            output, _, _ = model(input_batch.transpose(1, 2))
            probabilities = torch.softmax(output, dim=1)

            probabilities_list.append(probabilities.cpu())

    output_tensor = torch.cat(probabilities_list).view(
        len(perturbed_samples), 1, -1
    )

    return output_tensor

"""## Step 7: Surrogate Model

### Build Surrogate Model
"""

from sklearn.linear_model import LinearRegression, BayesianRidge

def build_surrogate_model(
    model_type: Literal["linear", "bayesian"] = "linear",
):
    """
    Initialize surrogate model.

    Args:
        model_type (str): Type of model ("linear" or "bayesian").

    Returns:
        sklearn model instance.
    """
    if model_type == "linear":
        return LinearRegression()
    elif model_type == "bayesian":
        return BayesianRidge()
    else:
        raise ValueError("model_type must be 'linear' or 'bayesian'")

"""### Calculate Coefficients"""

def calculate_surrogate_coefficients(
    model_type: str,
    cluster_masks: np.ndarray,
    output_probabilities: torch.Tensor,
    top_pred_classes: np.ndarray,
    weights: np.ndarray,
) -> Tuple[np.ndarray, np.ndarray, np.ndarray]:
    """
    Fit surrogate model and return coefficients and predictions.

    Args:
        model_type (str): Surrogate model type.
        cluster_masks (np.ndarray): Binary interpretable features.
        output_probabilities (torch.Tensor): Model outputs.
        top_pred_classes (np.ndarray): Top predicted classes.
        weights (np.ndarray): Sample weights.

    Returns:
        Tuple:
            - coefficients
            - y_true
            - y_pred
    """
    class_idx = top_pred_classes[0]

    probs_np = output_probabilities.detach().cpu().numpy().astype(np.float32)
    y_true = probs_np[:, :, class_idx].ravel()

    surrogate_model = build_surrogate_model(model_type)

    surrogate_model.fit(
        X=cluster_masks,
        y=y_true,
        sample_weight=weights,
    )

    y_pred = surrogate_model.predict(cluster_masks).ravel()

    # Handle coef shape difference
    coef = (
        surrogate_model.coef_[0]
        if model_type == "linear"
        else surrogate_model.coef_
    )

    return coef, y_true, y_pred

"""### Calculate Fidelity Metrics"""

def calculate_fidelity_metrics(
    y_true: np.ndarray,
    y_pred: np.ndarray,
    weights: np.ndarray,
    cluster_masks: np.ndarray,
    model_name: str,
    num_clusters: int,
    kernel_width: float,
    num_perturbations: int,
) -> Dict:
    """
    Compute fidelity metrics for surrogate model.

    Returns:
        Dict: Metrics dictionary.
    """
    mse = mean_squared_error(y_true, y_pred, sample_weight=weights)
    r2 = r2_score(y_true, y_pred, sample_weight=weights)
    mae = mean_absolute_error(y_true, y_pred, sample_weight=weights)

    mean_loss = abs(np.mean(y_true) - np.mean(y_pred))
    mean_l1 = np.mean(np.abs(y_true - y_pred))
    mean_l2 = np.mean((y_true - y_pred) ** 2)

    weighted_l1 = np.sum(weights * np.abs(y_true - y_pred)) / len(y_true)
    weighted_l2 = np.sum(weights * (y_true - y_pred) ** 2) / len(y_true)

    f_mean = np.average(y_true, weights=weights)
    ss_tot = np.sum(weights * (y_true - f_mean) ** 2)
    ss_res = np.sum(weights * (y_true - y_pred) ** 2)
    weighted_r2 = 1 - ss_res / ss_tot

    n = len(y_true)
    p = cluster_masks.shape[1]
    weighted_adj_r2 = 1 - (1 - weighted_r2) * (n - 1) / (n - p - 1)

    return {
        "name": model_name,
        "num_clusters": num_clusters,
        "mse": mse,
        "r2": r2,
        "mae": mae,
        "mean_loss": mean_loss,
        "mean_l1": mean_l1,
        "mean_l2": mean_l2,
        "weighted_l1": weighted_l1,
        "weighted_l2": weighted_l2,
        "weighted_r2": weighted_r2,
        "weighted_adj_r2": weighted_adj_r2,
        "kernel_width": kernel_width,
        "perturbation": num_perturbations,
    }

"""### Print Fidelity Metrics"""

def print_fidelity_metrics(metrics: dict) -> None:
    """
    Print fidelity metrics in a formatted way.

    Args:
        metrics (dict): Metrics dictionary.
    """
    print("-" * 100)
    print("Fidelity:")
    print(f"Mean Squared Error (MSE): {metrics['mse']}")
    print(f"R-squared (R²): {metrics['r2']}")
    print(f"Mean Absolute Error (MAE): {metrics['mae']}")
    print(f"Mean Loss (Lm): {metrics['mean_loss']}")
    print(f"Mean L1 Loss: {metrics['mean_l1']}")
    print(f"Mean L2 Loss: {metrics['mean_l2']}")
    print(f"Weighted L1 Loss: {metrics['weighted_l1']}")
    print(f"Weighted L2 Loss: {metrics['weighted_l2']}")
    print(f"Weighted R-squared (R²ω): {metrics['weighted_r2']}")
    print(f"Weighted Adjusted R-squared (Rˆ²ω): {metrics['weighted_adj_r2']}")
    print("-" * 100)

