def generate_rotation_frames(
    radius: float = 2.0,
    height: float = 0.8,
    num_steps: int = 100,
) -> List[Dict[str, Any]]:
    """Generates camera rotation frames for 3D animations.

    Args:
        radius: Distance of the camera from the origin in the XY plane.
        height: Z-axis position of the camera.
        num_steps: Number of animation frames.

    Returns:
        A list of Plotly animation frame dictionaries for a rotating view.
    """
    angles = np.linspace(0, 2 * np.pi, num_steps)
    frames = []

    for angle in angles:
        frame = dict(
            layout=dict(
                scene=dict(
                    camera=dict(
                        eye=dict(
                            x=radius * np.cos(angle),
                            y=radius * np.sin(angle),
                            z=height,
                        )
                    )
                )
            )
        )
        frames.append(frame)

    return frames


def create_mesh_figure(
    vertices: np.ndarray,
    faces: np.ndarray,
    opacity: float = 0.5,
) -> go.Figure:
    """Creates a 3D mesh visualization with a rotation animation.

    Args:
        vertices: Array of vertices of shape (N, 3).
        faces: Array of faces of shape (F, 3).
        opacity: Transparency level of the mesh (0.0 to 1.0).

    Returns:
        A Plotly figure containing the animated mesh.
    """
    x, y, z = vertices.T
    i, j, k = faces.T

    mesh = go.Mesh3d(
        x=x,
        y=y,
        z=z,
        i=i,
        j=j,
        k=k,
        opacity=opacity,
    )

    fig = go.Figure(
        data=[mesh],
        frames=generate_rotation_frames(),
        layout=dict(
            updatemenus=[
                dict(
                    type="buttons",
                    showactive=False,
                    buttons=[
                        dict(
                            label="Play",
                            method="animate",
                            args=[None],
                        )
                    ],
                )
            ]
        ),
    )

    return fig


