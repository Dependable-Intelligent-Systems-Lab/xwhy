def visualize_explanation_pointcloud(
    points: np.ndarray,
    cluster_labels: np.ndarray,
    important_clusters: np.ndarray,
    base_color: str = "blue",
    highlight_color: str = "red",
) -> go.Figure:
    """
    Visualize explanation over point cloud by highlighting important clusters.

    Args:
        points (np.ndarray): Point cloud of shape (N, 3).
        cluster_labels (np.ndarray): Cluster index per point.
        important_clusters (np.ndarray): Selected influential clusters.
        base_color (str): Default color for points.
        highlight_color (str): Color for important clusters.

    Returns:
        go.Figure: Plotly figure.
    """
    colors = np.full(cluster_labels.shape, base_color)

    for cluster_id in important_clusters:
        colors[cluster_labels == cluster_id] = highlight_color

    trace = create_point_cloud_trace(
        xs=points[:, 0],
        ys=points[:, 1],
        zs=points[:, 2],
        color=colors,
        name="Explanation Point Cloud",
    )

    fig = go.Figure(
        data=[trace],
        layout=create_clean_3d_layout(),
    )

    return fig

